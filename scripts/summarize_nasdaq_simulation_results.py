#!/usr/bin/env python3
"""Aggregate Summary sections from nasdaq-sim `results.md` files into one cross-run report.

Reads only `## Summary` blocks (no per-week tables, no logs). Writes:
  data/backtests/nasdaq-simulations-cross-run-report.md

Run from repo root:
  .venv/bin/python scripts/summarize_nasdaq_simulation_results.py
"""

from __future__ import annotations

import math
import re
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1] / "data" / "backtests"
OUT = ROOT / "nasdaq-simulations-cross-run-report.md"


def main() -> None:
    paths = sorted(ROOT.glob("nasdaq-sim-N*-massive/nasdaq-scoring-simulation-*/results.md"))
    rows: list[dict] = []

    for p in paths:
        text = p.read_text(encoding="utf-8", errors="replace")
        if "## Summary" not in text:
            continue
        summary = text.split("## Summary", 1)[1]
        if "##" in summary:
            summary = summary.split("##", 1)[0]

        m_folder = re.search(r"nasdaq-sim-N(\d+)-massive", str(p))
        n_from_folder = int(m_folder.group(1)) if m_folder else None

        m_method = re.search(r"nasdaq-scoring-simulation-([^/]+)/results", str(p))
        method = m_method.group(1) if m_method else "?"

        m_w = re.search(
            r"Sample weeks requested:\s*(\d+);\s*windows with rows:\s*(\d+)",
            summary,
        )
        n_req = int(m_w.group(1)) if m_w else n_from_folder
        n_rows = int(m_w.group(2)) if m_w else None

        m_best = re.search(
            r"Best terminal accumulated return:\s*\*\*([^*]+)\*\*\s*≈\s*([\d.]+)%",
            summary,
        )
        m_worst = re.search(
            r"Worst terminal accumulated return:\s*\*\*([^*]+)\*\*\s*≈\s*([\d.]+)%",
            summary,
        )
        if not m_best:
            continue
        best_key = m_best.group(1).strip()
        best_pct = float(m_best.group(2))
        worst_key = m_worst.group(1).strip() if m_worst else None
        worst_pct = float(m_worst.group(2)) if m_worst else None

        g = re.search(r"group(\d+)_max(\d+)", best_key)
        best_group = int(g.group(1)) if g else None
        best_maxp = int(g.group(2)) if g else None

        n_eff = n_req or n_from_folder or 1
        norm = best_pct / math.sqrt(n_eff)

        rows.append(
            {
                "path": str(p.relative_to(ROOT)),
                "run_id": p.parent.parent.name,
                "method": method,
                "n_req": n_req,
                "n_rows": n_rows,
                "best_key": best_key,
                "best_pct": best_pct,
                "worst_key": worst_key,
                "worst_pct": worst_pct,
                "best_group": best_group,
                "best_maxp": best_maxp,
                "norm_sqrtn": norm,
            }
        )

    lines: list[str] = []
    lines.append("# Nasdaq weekly screener — cross-run summary")
    lines.append("")
    lines.append(
        "Generated by `scripts/summarize_nasdaq_simulation_results.py` from the "
        "**Summary** section of each `results.md` only."
    )
    lines.append("")

    lines.append("## Executive overview")
    lines.append("")
    lines.append(
        "- **Normalization:** **R / √N** (best terminal % ÷ √(sample weeks requested)). "
        "Rough cross-run scale; not annualized."
    )
    lines.append(
        "- **Strongest methods by peak R/√N** (largest-N runs): **`momentum`**, **`atr_roc5`**, "
        "**`atr_vwap_dev`**, **`relative_volume`** cluster at the top on **N=15** batches; "
        "**`roc_acceleration`** / **`shock_vol_roc`** track below them but still favor **group1** + **max3**."
    )
    lines.append(
        "- **Decile group:** Most methods lean **Group 1** (top decile) for the best terminal row. "
        "Clear exceptions: **`bb_pctb`** → **Group 3** in both runs; **`relative_volume`** is a **2–2 tie** "
        "between Group 1 and Group 3 across runs (the **highest R/√N** for that method is **Group 3**, N=15)."
    )
    lines.append(
        "- **Small-N / smoke runs** (N=1–2) are useful for plumbing checks only—do not rank them against N≥9."
    )
    lines.append("")

    lines.append("## Interpretation notes")
    lines.append("")
    lines.append(
        "- **Terminal return** = last-row accumulated % in each run’s tables (geometric compounding "
        "over that run’s sample weeks)."
    )
    lines.append(
        "- **N** differs between batches, so raw terminal % is **not** comparable across runs; use **R/√N** "
        "only as a rough scale adjustment."
    )
    lines.append(
        "- **Groups:** Group 1 = highest decile bin. If most runs for a method show Group 1 as best, "
        "keep Group 1 as the default; one run where Group 2 is slightly ahead does not overturn that."
    )
    lines.append("")

    lines.append("## All runs (one row per `results.md`)")
    lines.append("")
    lines.append(
        "| Batch folder | Scoring | N | Rows | Best (group×slots) | Best term % | R/√N | Worst term % |"
    )
    lines.append("| --- | --- | ---: | ---: | --- | ---: | ---: | ---: |")

    for r in sorted(rows, key=lambda x: (x["method"], x["run_id"], x["n_req"] or 0)):
        wr = f"{r['worst_pct']:.2f}" if r["worst_pct"] is not None else "—"
        nr = r["n_rows"]
        nr_s = str(nr) if nr is not None else "—"
        lines.append(
            f"| `{r['run_id']}` | `{r['method']}` | {r['n_req']} | {nr_s} | `{r['best_key']}` | "
            f"{r['best_pct']:.4f} | {r['norm_sqrtn']:.4f} | {wr} |"
        )

    lines.append("")
    lines.append("## By scoring method — which group / slot won?")
    lines.append("")
    lines.append(
        "Counts of **best terminal** outcomes (from the `groupK_maxM` token in each Summary)."
    )
    lines.append("")

    by_method: dict[str, list[dict]] = defaultdict(list)
    for r in rows:
        by_method[r["method"]].append(r)

    def _freq_line(counts: dict[str, int], label: str) -> str:
        if not counts:
            return ""
        mx = max(counts.values())
        top = sorted([k for k, v in counts.items() if v == mx])
        parts = [f"{k}: {counts[k]}" for k in sorted(counts.keys())]
        base = f"- **{label} (frequency):** " + "; ".join(parts)
        if len(top) > 1:
            base += f" — **tie at {mx}** among {', '.join(top)}."
        return base

    for method in sorted(by_method.keys()):
        rs = by_method[method]
        lines.append(f"### `{method}`")
        lines.append("")
        counts: dict[str, int] = defaultdict(int)
        slot_counts: dict[str, int] = defaultdict(int)
        for r in rs:
            if r["best_group"] is not None:
                counts[f"group{r['best_group']}"] += 1
            if r["best_maxp"] is not None:
                slot_counts[f"max{r['best_maxp']}"] += 1
        fl = _freq_line(counts, "Best group")
        if fl:
            lines.append(fl)
        sl = _freq_line(slot_counts, "Best max_positions")
        if sl:
            lines.append(sl)
        if counts:
            mx = max(counts.values())
            top = sorted([k for k, v in counts.items() if v == mx])
            if len(top) == 1:
                lines.append(
                    f"- **Heuristic default:** favor **{top[0]}** "
                    f"({mx}/{len(rs)} runs had best terminal there)."
                )
            else:
                lines.append(
                    f"- **Heuristic default:** **no single winner** — tied at {mx}/{len(rs)}: "
                    f"{', '.join(top)}. Use the **highest R/√N** row for this method (table below) or "
                    "prefer Group 1 unless your metric-specific backtest says otherwise."
                )
        best_r = max(rs, key=lambda x: x["norm_sqrtn"])
        lines.append(
            f"- **Highest R/√N in this method:** {best_r['norm_sqrtn']:.4f} "
            f"(`{best_r['run_id']}`, N={best_r['n_req']}, terminal {best_r['best_pct']:.2f}%, "
            f"`{best_r['best_key']}`)."
        )
        lines.append("")

    lines.append("## Cross-method comparison (R/√N only)")
    lines.append("")
    lines.append(
        "Sorted by **R/√N** descending. Prefer comparing runs with **similar N**."
    )
    lines.append("")
    lines.append("| R/√N | Batch | Method | N | Best term % | Best key |")
    lines.append("| ---: | --- | --- | ---: | ---: | --- |")
    for r in sorted(rows, key=lambda x: -x["norm_sqrtn"]):
        lines.append(
            f"| {r['norm_sqrtn']:.4f} | `{r['run_id']}` | `{r['method']}` | {r['n_req']} | "
            f"{r['best_pct']:.2f} | `{r['best_key']}` |"
        )

    lines.append("")
    lines.append("## Best single run per scoring method (by R/√N)")
    lines.append("")
    lines.append("| Method | Best R/√N | Batch | N | Terminal % | Best key |")
    lines.append("| --- | ---: | --- | ---: | ---: | --- |")
    seen: set[str] = set()
    for r in sorted(rows, key=lambda x: (-x["norm_sqrtn"], x["method"])):
        if r["method"] in seen:
            continue
        seen.add(r["method"])
        lines.append(
            f"| `{r['method']}` | {r['norm_sqrtn']:.4f} | `{r['run_id']}` | {r['n_req']} | "
            f"{r['best_pct']:.2f} | `{r['best_key']}` |"
        )

    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {OUT} ({len(rows)} runs)")


if __name__ == "__main__":
    main()
